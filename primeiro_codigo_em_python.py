# Primeiro código em Python.
print("Olá Mundo!")
print("Bem-vindo ao Python.")

# Por convenção, constantes são escritas em maiúsculas.
NOME = "João"

# Variáveis podem ser escritas em minúsculas. E é utilizado o padrão snake_case.
idade_usuario = 21

# Salvando variáveis em uma mesma linha.
estado_residencia, cidade_residencia = "Pernambuco", "Recife"
print(f"Olá, meu nome é {NOME} e eu tenho {str(idade_usuario)} anos. Eu moro e {cidade_residencia}, {estado_residencia}.")

# Exibindo o tipo da variável do NOME.
print(type(NOME))